import requests
import json

def test_language(language_code, essay_text, language_name):
    """Test specific language"""
    
    url = "http://localhost:8000/grade"
    
    payload = {
        "essay": essay_text,
        "selected_language": language_code
    }
    
    headers = {
        "Content-Type": "application/json; charset=utf-8"
    }
    
    print(f"🧪 TESTING {language_name.upper()}")
    print("=" * 60)
    print(f"Essay length: {len(essay_text)} characters")
    print(f"Word count: {len(essay_text.split())} words")
    
    try:
        response = requests.post(url, json=payload, headers=headers)
        print(f"Response status: {response.status_code}")
        
        if response.status_code == 200:
            result = response.json()
            print("✅ SUCCESS!")
            print(f"Predicted score: {result.get('predicted_score', 'N/A')}")
            print(f"Detected language: {result.get('detected_language', 'N/A')}")
            print(f"Language compliance: {result.get('language_compliance', {}).get('is_compliant', 'N/A')}")
            print(f"Feedback preview: {result.get('strict_response', '')[:100]}...")
        else:
            print("❌ FAILED!")
            print(f"Error: {response.text}")
            
    except Exception as e:
        print(f"❌ EXCEPTION: {e}")
    
    print("\n")

